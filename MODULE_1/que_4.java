package MODULE_1;
//que-4.Write a program in Java to display the first 10
//natural numbers using while loop. 
public class que_4 {
	 public static void main(String[] args)
	 {     
		 int i;
		 System.out.println ("First 10 natural numbers");
		 for (i=1;i<=10;i++)
		 {      
			System.out.println (i);
		 }
	 }
}
